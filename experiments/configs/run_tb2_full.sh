#!/usr/bin/env bash
# run_tb2_full.sh — TB 2.0 全量 89-task SkillQ 实验启动脚本
#
# 配置: experiments/configs/tb2_skillq_full.yaml
#   Agent:     deepseek-v4-flash (来自 .env ANTHROPIC_MODEL)
#   Embedding: text-embedding-v4  (来自 .env EMBEDDING_MODEL)
#
#   L1: multiplicative, β=0.5, γ=0.2, sim_gate=0.7, floor=0, top_k=3
#   L3: attribution + editor = deepseek-v4-flash
#   L4: extract_every_n_trials=1, dedup=0.85, enforce_failure_structure=true
#   Q:  fresh-start (清 Q-table + emb_cache), b_max=1000, seed_q=0.5
#
#   89 tasks × 1 attempt, 8 并发, 1h/trial, delete=true (跑完即删容器)
#   预计时长 6-9h, 产物 output/tb2_skillq_full__<timestamp>/
#
# 用法:
#   # 前台 (Ctrl+C 终止)
#   bash experiments/configs/run_tb2_full.sh
#
#   # 后台 nohup (推荐 — ssh 断连不中断, 日志落盘)
#   nohup bash experiments/configs/run_tb2_full.sh > /tmp/tb2_full.log 2>&1 &
#   tail -f /tmp/tb2_full.log

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "$SCRIPT_DIR/../.." && pwd)"
cd "$REPO_ROOT"

# ------------------------------------------------------------------
# pre-flight
# ------------------------------------------------------------------
echo "[$(date '+%H:%M:%S')] === pre-flight ==="

# 1. Docker daemon
if ! docker info >/dev/null 2>&1; then
    echo "[$(date '+%H:%M:%S')] FATAL: Docker daemon not running."
    exit 1
fi
echo "[$(date '+%H:%M:%S')] Docker: UP"

# 2. .env
if [ ! -f .env ]; then
    echo "[$(date '+%H:%M:%S')] FATAL: .env not found."
    exit 1
fi
# shellcheck source=/dev/null
source .env
for key in ANTHROPIC_AUTH_TOKEN ANTHROPIC_BASE_URL ANTHROPIC_MODEL EMBEDDING_API_KEY EMBEDDING_BASE_URL EMBEDDING_MODEL; do
    if [ -z "${!key:-}" ]; then
        echo "[$(date '+%H:%M:%S')] FATAL: .env ${key} is empty."
        exit 1
    fi
done
echo "[$(date '+%H:%M:%S')] .env: OK (model=${ANTHROPIC_MODEL})"

# 3. Docker images
IMG_COUNT=$(docker images --format '{{.Repository}}' 2>/dev/null | grep -c '^skills_vote/' || true)
if [ "$IMG_COUNT" -lt 80 ]; then
    echo "[$(date '+%H:%M:%S')] FATAL: only ${IMG_COUNT} skills_vote images (need >= 80)."
    exit 1
fi
echo "[$(date '+%H:%M:%S')] Images: ${IMG_COUNT} skills_vote/* present"

# 4. Dry-run (验证 YAML parse + split 无报错)
echo "[$(date '+%H:%M:%S')] Dry-run..."
if ! timeout 30 uv run python experiments/run/run_benchmark.py \
    --benchmark tb2 --variant full --fresh-start --dry-run >/tmp/tb2_full_dry.log 2>&1; then
    echo "[$(date '+%H:%M:%S')] FATAL: dry-run failed (see /tmp/tb2_full_dry.log)"
    exit 1
fi
# clean up split artifacts generated by dry-run
rm -f experiments/configs/tb2_skillq_full__*.job.yaml experiments/configs/tb2_skillq_full__*.method.yaml
echo "[$(date '+%H:%M:%S')] Dry-run: OK"

# ------------------------------------------------------------------
# launch
# ------------------------------------------------------------------
echo ""
echo "[$(date '+%H:%M:%S')] === launching full experiment ==="
echo "[$(date '+%H:%M:%S')] 89 tasks, 8 concurrent, fresh-start, extract_every_n_trials=1"
echo "[$(date '+%H:%M:%S')] model: ${ANTHROPIC_MODEL}"
echo ""

uv run python experiments/run/run_benchmark.py \
    --benchmark tb2 \
    --variant full \
    --fresh-start

echo ""
echo "[$(date '+%H:%M:%S')] === experiment finished ==="
