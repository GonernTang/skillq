#!/usr/bin/env bash
# run_tb2_e2e.sh — TB 2.0 3-task SkillQ e2e 启动脚本
#
# 配置: experiments/configs/tb2_skillq_e2e.yaml
#   Agent:     deepseek-v4-flash (来自 .env ANTHROPIC_MODEL)
#   Embedding: text-embedding-v4  (来自 .env EMBEDDING_MODEL)
#
#   L1: multiplicative, β=0.5, γ=0.2, sim_gate=0.7, floor=0, top_k=3
#   L3: attribution + editor = deepseek-v4-flash
#   L4: extract_every_n_trials=1, enforce_failure_structure=true
#   Q:  reuse_q_table=true, reuse_embedding_cache=true, b_max=1000, seed_q=0.5
#
#   3 tasks (adaptive-rejection-sampler, bn-fit-modify, break-filter-js-from-html)
#   × 1 attempt, 3 并发, 1h/trial, delete=true (跑完即删容器)
#   预计 30-90 min, 产物 output/tb2_skillq_e2e__<timestamp>/
#
# 用途: 验证 4-layer pipeline (L1 retrieval → L2 agent → L3 attribution →
# L4 evolve) 在真实任务上是否正常触发与落盘。可作为 full run 前的冒烟,
# 也可作为任何 layer 改动后的回归验证。
#
# 用法:
#   # 前台 (Ctrl+C 终止)
#   bash experiments/configs/run_tb2_e2e.sh
#
#   # 后台 nohup (推荐 — ssh 断连不中断, 日志落盘)
#   nohup bash experiments/configs/run_tb2_e2e.sh > /tmp/tb2_e2e.log 2>&1 &
#   tail -f /tmp/tb2_e2e.log

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "$SCRIPT_DIR/../.." && pwd)"
cd "$REPO_ROOT"

# ------------------------------------------------------------------
# pre-flight
# ------------------------------------------------------------------
echo "[$(date '+%H:%M:%S')] === pre-flight ==="

# 1. Docker daemon
if ! docker info >/dev/null 2>&1; then
    echo "[$(date '+%H:%M:%S')] FATAL: Docker daemon not running."
    exit 1
fi
echo "[$(date '+%H:%M:%S')] Docker: UP"

# 2. .env
if [ ! -f .env ]; then
    echo "[$(date '+%H:%M:%S')] FATAL: .env not found."
    exit 1
fi
# shellcheck source=/dev/null
source .env
for key in ANTHROPIC_AUTH_TOKEN ANTHROPIC_BASE_URL ANTHROPIC_MODEL EMBEDDING_API_KEY EMBEDDING_BASE_URL EMBEDDING_MODEL; do
    if [ -z "${!key:-}" ]; then
        echo "[$(date '+%H:%M:%S')] FATAL: .env ${key} is empty."
        exit 1
    fi
done
echo "[$(date '+%H:%M:%S')] .env: OK (model=${ANTHROPIC_MODEL})"

# 3. Docker images — only need the 3 chosen tasks (full run guards ≥80)
NEEDED_TASKS=(configure-git-webserver path-tracing)
MISSING=()
for t in "${NEEDED_TASKS[@]}"; do
    if ! docker image inspect "skills_vote/${t}:20260604" >/dev/null 2>&1; then
        MISSING+=("$t")
    fi
done
if [ ${#MISSING[@]} -gt 0 ]; then
    echo "[$(date '+%H:%M:%S')] FATAL: missing skills_vote images for: ${MISSING[*]}"
    exit 1
fi
echo "[$(date '+%H:%M:%S')] Images: all 3 skills_vote/<task>:20260604 present"

# 4. Dry-run (验证 YAML parse + split 无报错)
echo "[$(date '+%H:%M:%S')] Dry-run..."
if ! timeout 30 uv run python experiments/run/run_benchmark.py \
    --benchmark tb2 --variant e2e --dry-run >/tmp/tb2_e2e_dry.log 2>&1; then
    echo "[$(date '+%H:%M:%S')] FATAL: dry-run failed (see /tmp/tb2_e2e_dry.log)"
    cat /tmp/tb2_e2e_dry.log
    exit 1
fi
# clean up split artifacts generated by dry-run
rm -f experiments/configs/tb2_skillq_e2e__*.job.yaml experiments/configs/tb2_skillq_e2e__*.method.yaml
echo "[$(date '+%H:%M:%S')] Dry-run: OK"

# ------------------------------------------------------------------
# launch
# ------------------------------------------------------------------
echo ""
echo "[$(date '+%H:%M:%S')] === launching 3-task e2e ==="
echo "[$(date '+%H:%M:%S')] tasks: ${NEEDED_TASKS[*]}"
echo "[$(date '+%H:%M:%S')] 3 concurrent, extract_every_n_trials=1, fresh-start=NO (reuse Q + emb_cache)"
echo "[$(date '+%H:%M:%S')] model: ${ANTHROPIC_MODEL}"
echo ""

uv run python experiments/run/run_benchmark.py \
    --benchmark tb2 \
    --variant e2e

echo ""
echo "[$(date '+%H:%M:%S')] === experiment finished ==="
