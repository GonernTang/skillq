#!/usr/bin/env bash
# run_tb2_small10.sh — TB 2.0 10-task SkillQ 小批量实验启动脚本
# (89-task full run 的前置 dress rehearsal)。
#
# 配置: experiments/configs/tb2_skillq_small10.yaml
#   Agent:     deepseek-v4-flash (来自 .env ANTHROPIC_MODEL)
#   Embedding: text-embedding-v4  (来自 .env EMBEDDING_MODEL)
#
#   L1: multiplicative, β=0.5, γ=0.2, sim_gate=0.5, floor=0, top_k=3
#   L3: attribution + editor = deepseek-v4-flash
#   L4: extract_every_n_trials=1, enforce_failure_structure=true
#   Q:  fresh-start (清 Q-table + emb_cache), b_max=1000, seed_q=0.5
#
#   10 tasks (curated, all have skills_vote/<task>:20260604 image)
#   × 1 attempt, 4 并发 (full 的一半), 1h/trial, delete=true
#   预计 45-75 min, 产物 output/tb2_skillq_small10__<timestamp>/
#
# 选这 10 个的理由:
#   - 覆盖 8 个 category:  game/algorithm, math, data, binary,
#     security, VCS×2, PL, ML
#   - 排除 6 个没有 skills_vote image 的: adaptive-rejection-sampler,
#     bn-fit-modify, break-filter-js-from-html, configure-git-webserver,
#     path-tracing, pytorch-model-recovery (需额外 retag 才能跑)
#
# 用法:
#   # 前台 (Ctrl+C 终止)
#   bash experiments/configs/run_tb2_small10.sh
#
#   # 后台 nohup (推荐 — ssh 断连不中断, 日志落盘)
#   nohup bash experiments/configs/run_tb2_small10.sh > /tmp/tb2_small10.log 2>&1 &
#   tail -f /tmp/tb2_small10.log

set -euo pipefail

SCRIPT_DIR="$(cd "$(dirname "${BASH_SOURCE[0]}")" && pwd)"
REPO_ROOT="$(cd "$SCRIPT_DIR/../.." && pwd)"
cd "$REPO_ROOT"

# ------------------------------------------------------------------
# pre-flight
# ------------------------------------------------------------------
echo "[$(date '+%H:%M:%S')] === pre-flight ==="

# 1. Docker daemon
if ! docker info >/dev/null 2>&1; then
    echo "[$(date '+%H:%M:%S')] FATAL: Docker daemon not running."
    exit 1
fi
echo "[$(date '+%H:%M:%S')] Docker: UP"

# 2. .env
if [ ! -f .env ]; then
    echo "[$(date '+%H:%M:%S')] FATAL: .env not found."
    exit 1
fi
# shellcheck source=/dev/null
source .env
for key in ANTHROPIC_AUTH_TOKEN ANTHROPIC_BASE_URL ANTHROPIC_MODEL EMBEDDING_API_KEY EMBEDDING_BASE_URL EMBEDDING_MODEL; do
    if [ -z "${!key:-}" ]; then
        echo "[$(date '+%H:%M:%S')] FATAL: .env ${key} is empty."
        exit 1
    fi
done
echo "[$(date '+%H:%M:%S')] .env: OK (model=${ANTHROPIC_MODEL})"

# 3. Docker images — only need the 10 chosen skills_vote/* tags
NEEDED_TASKS=(
  caffe-cifar-10
  cobol-modernization
  code-from-image
  constraints-scheduling
  db-wal-recovery
  distribution-search
  extract-moves-from-video
  feal-differential-cryptanalysis
  gcode-to-text
  log-summary-date-ranges
)
MISSING=()
for t in "${NEEDED_TASKS[@]}"; do
    if ! docker image inspect "skills_vote/${t}:20260604" >/dev/null 2>&1; then
        MISSING+=("$t")
    fi
done
if [ ${#MISSING[@]} -gt 0 ]; then
    echo "[$(date '+%H:%M:%S')] FATAL: missing skills_vote images for: ${MISSING[*]}"
    exit 1
fi
echo "[$(date '+%H:%M:%S')] Images: all 10 skills_vote/<task>:20260604 present"

# 4. Dry-run (验证 YAML parse + split 无报错)
echo "[$(date '+%H:%M:%S')] Dry-run..."
if ! timeout 30 uv run python experiments/run/run_benchmark.py \
    --benchmark tb2 --variant small10 --fresh-start --dry-run >/tmp/tb2_small10_dry.log 2>&1; then
    echo "[$(date '+%H:%M:%S')] FATAL: dry-run failed (see /tmp/tb2_small10_dry.log)"
    cat /tmp/tb2_small10_dry.log
    exit 1
fi
# clean up split artifacts generated by dry-run
rm -f experiments/configs/tb2_skillq_small10__*.job.yaml \
      experiments/configs/tb2_skillq_small10__*.method.yaml
echo "[$(date '+%H:%M:%S')] Dry-run: OK"

# ------------------------------------------------------------------
# launch
# ------------------------------------------------------------------
echo ""
echo "[$(date '+%H:%M:%S')] === launching 10-task small batch ==="
echo "[$(date '+%H:%M:%S')] tasks: ${NEEDED_TASKS[*]}"
echo "[$(date '+%H:%M:%S')] 4 concurrent, fresh-start, extract_every_n_trials=1"
echo "[$(date '+%H:%M:%S')] model: ${ANTHROPIC_MODEL}"
echo ""

uv run python experiments/run/run_benchmark.py \
    --benchmark tb2 \
    --variant small10 \
    --fresh-start

EXIT_CODE=$?

echo ""
echo "[$(date '+%H:%M:%S')] === experiment finished (exit=$EXIT_CODE) ==="

# 2026-07-01: small10.yaml has delete: false so the skills_vote
# base images survive. Run the container-only cleanup so disk/mem
# is reclaimed. Best-effort: do not fail the wrapper if cleanup
# errors.
bash "$(dirname "${BASH_SOURCE[0]}")/_post_run_cleanup.sh" || \
    echo "[$(date '+%H:%M:%S')] WARN: post-run cleanup failed (non-fatal)"

exit "$EXIT_CODE"
